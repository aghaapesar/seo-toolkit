def build_tool_groups(lang: str) -> List[Dict[str, Any]]:
    """
    Group TOOLS for navigation and dashboard sections.

    Output:
        Ordered list of {id, label, tools}.
    """
    buckets: Dict[str, List[Dict[str, Any]]] = {g: [] for g in TOOL_GROUP_ORDER}
    for tool in TOOLS:
        group_id = TOOL_GROUP_MAP.get(tool["id"], "core")
        buckets.setdefault(group_id, []).append(tool)
    groups: List[Dict[str, Any]] = []
    for group_id in TOOL_GROUP_ORDER:
        items = buckets.get(group_id) or []
        if not items:
            continue
        label_key = TOOL_GROUP_LABEL_KEYS.get(group_id, "nav_group_core")
        groups.append(
            {
                "id": group_id,
                "label": t(lang, label_key),
                "tools": items,
            }
        )
    return groups


def get_lang(request) -> str:
    """Resolve language from query param or cookie."""
    lang = request.query_params.get("lang") or request.cookies.get("lang", "fa")
    return lang if lang in SUPPORTED_LANGS else "fa"


def t(lang: str, key: str) -> str:
    """Translate a key for the given language."""
    return STRINGS.get(lang, STRINGS["en"]).get(key, key)


def strings_for(lang: str) -> Dict[str, str]:
    """Merge EN baseline with locale strings so templates never get Undefined."""
    merged = dict(STRINGS["en"])
    merged.update(STRINGS.get(lang, {}))
    return merged


def page_context(request, page_title: str, **extra: Any) -> Dict[str, Any]:
    """Build standard template context."""
    from src import __version__
    from src.services.project_manager import ProjectManager
    from web.app.deps.auth import get_optional_user

    lang = get_lang(request)
    manager = ProjectManager()
    projects = manager.list_projects()
    active_slug = request.query_params.get("project") or request.cookies.get("active_project")
    active_project = manager.get_project(active_slug) if active_slug else None
    current_user = get_optional_user(request)

    return {
        "lang": lang,
        "dir": "rtl" if lang == "fa" else "ltr",
        "page_title": page_title,
        "strings": strings_for(lang),
        "tools": TOOLS,
        "tool_groups": build_tool_groups(lang),
        "version": __version__,
        "config_exists": Path_exists_config(),
        "tool_id": extra.pop("tool_id", None),
        "projects": projects,
        "active_project": active_project,
        "active_project_slug": active_slug or "",
        "current_user": current_user.to_dict() if current_user else None,
        **extra,
    }


def Path_exists_config() -> bool:
    """Check if config.yaml exists in project root."""
    from pathlib import Path

    root = Path(__file__).resolve().parents[2]
    return (root / "config.yaml").exists()
